# Note: In ID 17, missing values are rare, but we implement the logic for robustness
def regression_impute(data, target_field, predictor_field):
    missing_mask = data[target_field].isnull()
    if missing_mask.sum() > 0:
        # Train a simple linear model on non-missing rows
        train_data = data[~missing_mask]
        model = LinearRegression().fit(train_data[[predictor_field]], train_data[target_field])
        # Predict for missing rows
        predictions = model.predict(data[missing_mask][[predictor_field]])
        data.loc[missing_mask, target_field] = predictions
    return data

